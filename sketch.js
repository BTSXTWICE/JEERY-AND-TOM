var jerryOne,jerry;
var garden,gardenImg;
var tomOne,tomTwo,tomThree,tomFour, tom_running,tom_collided,tomLastImage,tom_still;

function preload() {
  tomLastImage=loadAnimation("images/tomFour.png");
  tom_still=loadAnimation("images/tomOne.png")
tom_running=loadAnimation("images/tomTwo.png","images/tomThree.png","images/tomFour.png");
tom_collided=loadAnimation("images/tomTwo.png");
gardenImg=loadImage("images/garden.png");
jerryOne=loadAnimation("images/jerryOne.png");
    
}

function setup(){
    createCanvas(600,600);
 tom=createSprite(450,480,20,50);
 tom.addAnimation("collided",tom_still);
 tom.addAnimation("running",tom_running);
 tom.addAnimation("LastImage", tomLastImage);
 tom.scale=0.1;
// tom.velocity=2;

// garden=createSprite(0,300,1200,1200);
 //garden.addImage("garden",gardenImg);
 //garden.scale=1

 jerry=createSprite(50,480,20,50);
 jerry.addAnimation("any",jerryOne)
jerry.scale=0.1;
jerry.velocityX=0;

tom.setCollider("circle",100,0,40);
tom.debug = false; 

 
}

function draw() {
  background(gardenImg);

  if(tom.x-jerry.x<(tom.width-jerry.width)/2){
   tom.velocityX=0
  
}

if(tom.isTouching(jerry)){
  tom.velocityX=0;
  tom.changeAnimation("LastImage",tomLastImage);
}
  drawSprites();
}


function keyPressed(){

  if(keyCode===LEFT_ARROW){
    tom.velocityX=-5;
    tom.changeAnimation("running",tom_running);
    
    tom.bounceOff(garden);
   
    
 
  }
  
    
  


}



